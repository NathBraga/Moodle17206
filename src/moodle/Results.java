     //making a list to create a class to bring all the informations in the Results database table for Java
     public ArrayList<ResultsUser> selectResultsInformations(){  
       
      ArrayList<ResultsUser> resultsInformations = new ArrayList<ResultsUser>();
      Connection connection = getConnection();
      
      String selection = "SELECT * FROM Results";
      Statement st;
      ResultSet rs;
      
      try{
          st = connection.createStatement();
          rs = st.executeQuery(selection);
          ResultsUser userResults;
          
          while(rs.next()){
              userResults = new ResultsUser(rs.getString("StudentID"), rs.getString("CourseSubject"), rs.getString("Exam"), rs.getDouble("StudentResults"));
              resultsInformations.add(userResults);
          }
      }catch (Exception e){
          e.printStackTrace();
      }
      return resultsInformations;
  }
    
